# 6-3 词汇表
vocabulary = {
    'print':'打印出指定的内容\n',       
    '=':'等号右边的值赋给等号左边的变量\n',
    '==':'一种表示两个变量相等的逻辑表达式\n',
    'pop()':'删除列汇中某个元素，需要在括号中加入删除元素在列表中的位置数字\n',
    'sort()':'永久性的按照字母为列表中的元素排序\n',
    }
print('print: ' + '\n\t' + vocabulary['print'])
print('=: ' + '\n\t' + vocabulary['='])
print('==: ' + '\n\t' + vocabulary['=='])
print('pop(): ' + '\n\t' + vocabulary['pop()'])
print('sort(): ' + '\n\t' + vocabulary['sort()'])
